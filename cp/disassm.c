static int da_snprintf(unsigned char *bytes, char *buf, int buflen,
		       unsigned char opcode, struct disassm_instruction *table)
{
#define IPFX "%-6s "

	int ilc = opcode >> 6;
	struct disassm_instruction *inst = &table[opcode];

	switch (inst->fmt) {
		case IF_INV:
			snprintf(buf, buflen, "??");
			break;
		case IF_VAR:
			{
				unsigned char subop; /* sub op-code */

				/* get the right byte */
				subop = *(bytes+(inst->loc/8));

				/* shift the needed portion right */
				subop >>= 8 - inst->len - (inst->loc%8);

				/* mask out any unneeded high bits */
				subop &= (1 << inst->len) - 1;

				snprintf(buf, buflen, "??? (many, X'%02X'+  "
					 "X'%02X', table = %p)", opcode, subop,
					 inst->u.ptr);
				if (inst->u.ptr)
					da_snprintf(bytes, buf, buflen, subop, inst->u.ptr);
				break;
			}
		case IF_E:
			snprintf(buf, buflen, IPFX,
				 inst->u.name);
			break;
		case IF_I:
			snprintf(buf, buflen, IPFX "X'%02X'",
				 inst->u.name,
				 *(bytes+1));				/* I  */
			break;
		case IF_RI1:
		case IF_RI2:
			snprintf(buf, buflen, IPFX "%s%d,%d",
				 inst->u.name,
				 (inst->fmt == IF_RI1 ? "R" : ""),	/* reg/mask */
				 *(bytes+1) >> 4,			/* R1/M1 */
				 *((unsigned short*)(bytes+2)));	/* I2 */
			break;
		case IF_RIL1:
		case IF_RIL2:
			snprintf(buf, buflen, IPFX "%s%d,%d",
				 inst->u.name,
				 (inst->fmt == IF_RIL1 ? "R" : ""),	/* reg/mask */
				 *(bytes+1) >> 4,			/* R1/M1 */
				 *((int*)(bytes+2)));			/* I2 */
			break;
		case IF_RR:
			snprintf(buf, buflen, IPFX "R%d,R%d",
				 inst->u.name,
				 *(bytes+1) >> 4,			/* R1 */
				 *(bytes+1) & 0xf);			/* R2 */
			break;
		case IF_RRE:
			snprintf(buf, buflen, IPFX "R%d,R%d",
				 inst->u.name,
				 *(bytes+3) >> 4,			/* R1 */
				 *(bytes+3) & 0xf);			/* R2 */
			break;
		case IF_RS1:
		case IF_RS2:
			snprintf(buf, buflen, IPFX "R%d,%s%d,%d(R%d)",
				 inst->u.name,
				 *(bytes+1) >> 4,			/* R1 */
				 (inst->fmt == IF_RS1 ? "R" : ""),	/* reg/mask */
				 *(bytes+1) & 0xf,			/* R3/M3 */
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D2 */
				 *(bytes+2) >> 4);			/* B2 */
			break;
		case IF_RSI:
			snprintf(buf, buflen, IPFX "R%d,R%d,%d",
				 inst->u.name,
				 *(bytes+1) >> 4,			/* R1 */
				 *(bytes+1) & 0xf,			/* R3 */
				 *((unsigned short*)(bytes+2)));	/* I2 */
			break;
		case IF_RSY1:
		case IF_RSY2:
			snprintf(buf, buflen, IPFX "R%d,%s%d,%d(R%d)",
				 inst->u.name,
				 *(bytes+1) >> 4,			/* R1 */
				 (inst->fmt == IF_RSY1 ? "R" : ""),	/* reg/mask */
				 *(bytes+1) & 0xf,			/* R3/M3 */
				 ((unsigned int)*((unsigned short*)(bytes+2)) & 0x0fff) +
				 (((unsigned int)*((unsigned char*)(bytes+4))) << 12),
									/* D2 */
				 *(bytes+2) >> 4);			/* B2 */
			break;
		case IF_RX:
			snprintf(buf, buflen, IPFX "R%d,%d(R%d,R%d)",
				 inst->u.name,
				 *(bytes+1) >> 4,			/* R1 */
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D2 */
				 *(bytes+1) & 0xf,			/* X2 */
				 *(bytes+2) >> 4);			/* B2 */
			break;
		case IF_RXY:
			snprintf(buf, buflen, IPFX "R%d,%d(R%d,R%d)",
				 inst->u.name,
				 *(bytes+1) >> 4,			/* R1 */
				 ((unsigned int)*((unsigned short*)(bytes+2)) & 0x0fff) +
				 (((unsigned int)*((unsigned char*)(bytes+4))) << 12),
									/* D2 */
				 *(bytes+1) & 0xf,			/* X2 */
				 *(bytes+2) >> 4);			/* B2 */
			break;
		case IF_S:
			snprintf(buf, buflen, IPFX "%d(R%d)",
				 inst->u.name,
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D2 */
				 *(bytes+2) >> 4);			/* B2 */
			break;
		case IF_SI:
			snprintf(buf, buflen, IPFX "%d(R%d),%d",
				 inst->u.name,
				 *(bytes+2) >> 4,			/* B1 */
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D1 */
				 *(bytes+1));				/* I2 */
			break;
		case IF_SS1:
			snprintf(buf, buflen, IPFX "%d(%d,R%d),%d(R%d)",
				 inst->u.name,
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D1 */
				 *(bytes+1)+1,				/* L  */
				 *(bytes+2) >> 4,			/* B1 */
				 *((unsigned short*)(bytes+4)) & 0x0fff,/* D2 */
				 *(bytes+4) >> 4);			/* B2 */
			break;
		case IF_SS2:
			snprintf(buf, buflen, IPFX "%d(%d,R%d),%d(%d,R%d)",
				 inst->u.name,
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D1 */
				 (*(bytes+1) >> 4) + 1,			/* L1 */
				 *(bytes+2) >> 4,			/* B1 */
				 *((unsigned short*)(bytes+4)) & 0x0fff,/* D2 */
				 (*(bytes+1) & 0xf) + 1,		/* L2 */
				 *(bytes+4) >> 4);			/* B2 */
			break;
		case IF_SS3:
			snprintf(buf, buflen, IPFX "%d(%d,R%d),%d(R%d),%d",
				 inst->u.name,
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D1 */
				 (*(bytes+1) >> 4) + 1,			/* L1 */
				 *(bytes+2) >> 4,			/* B1 */
				 *((unsigned short*)(bytes+4)) & 0x0fff,/* D2 */
				 *(bytes+4) >> 4,			/* B2 */
				 *(bytes+1) & 0xf);			/* I3 */
			break;
		case IF_SS4:
			snprintf(buf, buflen, IPFX "%d(%d,R%d),%d(R%d),R%d",
				 inst->u.name,
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D1 */
				 (*(bytes+1) >> 4) + 1,			/* L1 */
				 *(bytes+2) >> 4,			/* B1 */
				 *((unsigned short*)(bytes+4)) & 0x0fff,/* D2 */
				 *(bytes+4) >> 4,			/* B2 */
				 *(bytes+1) & 0xf);			/* R3 */
			break;
		case IF_SS5:
			snprintf(buf, buflen, IPFX "R%d,R%d,%d(R%d),%d(R%d)",
				 inst->u.name,
				 (*(bytes+1) >> 4) + 1,			/* R1 */
				 *(bytes+1) & 0xf,			/* R3 */
				 *((unsigned short*)(bytes+2)) & 0x0fff,/* D2 */
				 *(bytes+2) >> 4,			/* B2 */
				 *((unsigned short*)(bytes+4)) & 0x0fff,/* D4 */
				 *(bytes+4) >> 4);			/* B4 */
			break;
		default:
			snprintf(buf, buflen, "%s ???", inst->u.name);
			break;
	}

	/* return instruction length in bytes */
	return 2 * (ilc >= 2 ? ilc : ilc + 1);
}

int disassm(unsigned char *bytes, char *buf, int buflen)
{
	return da_snprintf(bytes, buf, buflen, *bytes, l1);
}
